
public class WhileUse1 {

	public static void main(String[] args) {
		System.out.println("------");
		int i=0;
		while(i<10) {
			System.out.println(i+" ");
			i++;
		}
		System.out.println("------");
		i=1;
		while(i<=10) {
			System.out.println(i+" ");
			i++;
		}
		System.out.println();
		
		System.out.println("------");
		char c2='a';
		while(c2<='z') {
			System.out.print(c2);
			System.out.print(",");
			c2++;
		}
		System.out.println();
		
		
		
		
		
		
	}

}
